def getPropertyDescNoSpaces():
    properties = getPropertiesWikiData()
    propertyNoSpaces = {}
    for key in properties:
        propertyNoSpaces[key] = key+"_"+properties[key].replace(" ", "_")
    properties = getPropertiesComplex()
    for key in properties:
        propertyNoSpaces[key] = key+"_"+properties[key].replace(" ", "_")

    return propertyNoSpaces

